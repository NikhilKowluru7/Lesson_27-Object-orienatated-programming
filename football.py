class football:
    def __init__(self,name,club):
        self.name = name
        self.club = club

Mbappe = football("Mbappe","Real Madrid")
Ronaldo = football("Ronaldo","Al Nassar")
Messi = football("Messi","Inter Miami")

print("The first football's name is: ",Mbappe.name,".Mbappe's club is",Mbappe.club)
print("The second football's name is: ",Ronaldo.name,".Ronaldo's club is",Ronaldo.club)
print("The third football's name is: ",Messi.name,".Messi's club is",Messi.club)